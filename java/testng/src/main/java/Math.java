
public class Math {

    final private double a;
    final private double b;

    public Math(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double sum() {
        return a + b;
    }

    public double div() {
        return a / b;
    }

    public double sub() {
        return a - b;
    }

    public double mul() {
        return a * b;
    }

}
